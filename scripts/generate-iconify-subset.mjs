import { mkdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const rootDir = join(dirname(fileURLToPath(import.meta.url)), '..');
const outputPath = join(
  rootDir,
  'src',
  'renderer',
  'src',
  'components',
  'miscellaneous',
  'iconCollections.generated.ts'
);

const usedIcons = {
  flag: ['cn-4x3', 'tr-4x3', 'us-4x3'],
  logos: [
    'brave',
    'chrome',
    'facebook',
    'firefox',
    'instagram-icon',
    'microsoft-edge',
    'opera',
    'reddit-icon',
    'safari',
    'soundcloud',
    'tiktok-icon',
    'twitch',
    'twitter',
    'vimeo-icon',
    'vivaldi',
    'youtube-icon'
  ]
};

function loadCollection(prefix) {
  return JSON.parse(
    readFileSync(join(rootDir, 'node_modules', '@iconify-json', prefix, 'icons.json'))
  );
}

function buildSubset() {
  const entries = [];

  for (const [prefix, names] of Object.entries(usedIcons)) {
    const collection = loadCollection(prefix);
    for (const name of names) {
      const icon = collection.icons?.[name];
      if (!icon) {
        throw new Error(`Missing ${prefix}:${name} in @iconify-json/${prefix}.`);
      }

      entries.push([`${prefix}:${name}`, icon]);
    }
  }

  entries.sort((left, right) => left[0].localeCompare(right[0]));
  return Object.fromEntries(entries);
}

function buildFileContent() {
  const subset = JSON.stringify(buildSubset(), null, 2);

  return `// Generated by npm run generate:icons
type IconifySubsetIcon = {
  body: string;
  width?: number;
  height?: number;
  left?: number;
  top?: number;
  hidden?: boolean;
};

export const ICONIFY_ICON_DATA: Record<string, IconifySubsetIcon> = ${subset};
`;
}

mkdirSync(dirname(outputPath), { recursive: true });
writeFileSync(outputPath, buildFileContent());
console.log(`Wrote ${outputPath}`);
